#include <string>
using namespace std;

int main() {
	int times;
	cin >> times;

	for (int i = 0; i < times; i++) {
		cout << " @@@   @@@ " << "\n";
		cout << "@   @ @   @" << "\n";
		cout << "@    @    @" << "\n";
		cout << "@         @" << "\n";
		cout << " @       @ " << "\n";
		cout << "  @     @  " << "\n";
		cout << "   @   @   " << "\n";
		cout << "    @ @    " << "\n";
		cout << "     @     " << "\n";
	}

	return 0;
}